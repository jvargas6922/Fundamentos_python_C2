# Operadores de comparación en Python
"""
 igual a (==): Verifica si dos valores son iguales.
 diferente de (!=): Verifica si dos valores son diferentes.
 mayor que (>): Verifica si un valor es mayor que otro.
 menor que (<): Verifica si un valor es menor que otro.
 mayor o igual que (>=): Verifica si un valor es mayor o igual que otro.
 menor o igual que (<=): Verifica si un valor es menor o igual que otro.

 Comparacion estricta
  identico === : Verifica si dos valores son iguales y del mismo tipo.
  no identico !== : Verifica si dos valores son diferentes o de diferente tipo.
"""
#ejemplo de comparación
a = 10
b = 20
print("a == b:", a == b)  # False 
print("a != b:", a != b)  # True 
print("a > b:", a > b)    # False
print("a < b:", a < b)    # True
print("a >= b:", a >= b)  # False
print("a <= b:", a <= b)  # True