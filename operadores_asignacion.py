# Operadores de Asignacion en Python.
"""
Asignacion simple: =
Asignacion con suma: +=
Asignacion con resta: -=
Asignacion con multiplicacion: *=
Asignacion con division: /=
Asignacion con exponenciaciÃ³n: **=
Asignacion con modulo: %=
Asignacion con division entera: //=
"""
# Asignacion simple
a = 10
print("Valor inicial de a:", a)

# Asignacion con suma
a += 5 # Equivalente a => a = a + 5
print("Despues de a += 5, a es:", a)

# Asignacion con resta
a -= 3 # Equivalente a => a = a - 3
print("Despues de a -= 3, a es:", a)

# Asignacion con multiplicacion
a *= 2 # Equivalente a => a = a * 2
print("Despues de a *= 2, a es:", a)

# Asignacion con division
a /= 4 # Equivalente a => a = a / 4
print("Despues de a /= 4, a es:", a)

# Asignacion con exponenciaciÃ³n
a **= 3 # Equivalente a => a = a ** 3
print("Despues de a **= 3, a es:", a)

# Asignacion con modulo
a %= 7 # Equivalente a => a = a % 7
print("Despues de a %= 7, a es:", a)